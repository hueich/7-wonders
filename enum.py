
class Age(object):
  I, II, III = range(3)

class Resource(object):
  # Basic Resources
  WOOD = 0
  STONE = 1
  ORE = 2
  CLAY = 3
  # Advanced Resources
  PAPYRUS = 4
  TEXTILE = 5
  GLASS = 6
  # Other
  COIN = 7

class Science(object):
  WHEEL, COMPASS, TABLET = range(3)

class Relation(object):
  SELF, LEFT, RIGHT = range(3)

class Direction(object):
  CLOCKWISE, COUNTERCLOCKWISE = range(2)
